import UIKit

extension UIColor {
    
    class var pbmsLightBlue: UIColor {
        get {
            return UIColor(red: 107.0/255.0, green: 209.0/255.0, blue: 1, alpha: 1)
        }
    }
    
    class var pbmsGray: UIColor {
        get {
            return UIColor(white: 55.0/255.0, alpha: 1)
        }
    }

    class var pbmsBlack: UIColor {
        get {
            return UIColor(white: 33.0/255.0, alpha: 1)
        }
    }
    
}
